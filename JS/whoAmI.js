var numOfSides = 6;


switch ( numOfSides ) {
    case 3:
        console.log("Triangle");
        break;
    case 4:
        console.log("Quadrilateral");
        break;
    case 5:
        console.log("Pentagon")
        break;
    case 6:
        console.log("Hexagon");
        break;
    default:
        console.log("Check Input")
        break;
}